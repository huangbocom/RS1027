import jieba
import jieba.analyse
import jieba.posseg as pseg
from textrank4zh import TextRank4Keyword, TextRank4Sentence

sentence1  = ""
file_object = open('news_高考.txt','r', encoding='UTF-8')
try: 
    for line in file_object:
         sentence1 = sentence1 + line
finally:
      file_object.close()

# 获取分词
seg_list = jieba.cut(sentence1, cut_all=False)
print(' '.join(seg_list))
# 获取分词和词性
words = pseg.cut(sentence1)
for word, flag in words:
	print('%s, %s' % (word, flag))



# 基于TextRank算法的关键词抽取
keywords = jieba.analyse.textrank(sentence1, topK=20, withWeight=True, allowPOS=('n', 'ns')) 

print(keywords)
for item in keywords:
    print(item[0],item[1])
    
# 输出重要的句子
tr4s = TextRank4Sentence()
tr4s.analyze(text=sentence1, lower=True, source = 'all_filters')
print('摘要：')
# 重要性较高的三个句子
for item in tr4s.get_key_sentences(num=3):
	# index是语句在文本中位置，weight表示权重
    print(item.index, item.weight, item.sentence)

